# Statistics from a Large Sample
class Solution:
    def sampleStats(self, count: [int]) -> [float]:
        minimum, maximum, median, mode, mode_freq = -1, 0, 0, 0, 0
        running_sum, running_cnt, low, high = 0, 0, -1, -1
        total_num_of_obj = sum(count)
        if total_num_of_obj % 2 == 0:
            l, h = (total_num_of_obj / 2) - 1, total_num_of_obj / 2
        else:
            l, h = total_num_of_obj // 2, total_num_of_obj // 2

        for i in range(256):
            if count[i] != 0:
                maximum = float(i)
                running_sum += i * count[i]
                running_cnt += count[i]
                if running_cnt > l and low == -1:
                    low = i
                if running_cnt > h and high == -1:
                    high = i
                if minimum == -1:
                    minimum = float(i)
                if count[i] > mode_freq:
                    mode = float(i)
                    mode_freq = count[i]

        mean = running_sum / total_num_of_obj
        median = (high + low) / 2

        return [minimum, maximum, mean, median, mode]


